async def _claim_compress(db):
    rs = await db.execute(
        "UPDATE kept_compress_jobs SET status='running', updated_at=now_text() "
        "WHERE id = (SELECT id FROM kept_compress_jobs WHERE status='queued' "
        "            ORDER BY id LIMIT 1 FOR UPDATE SKIP LOCKED) "
        "RETURNING id, kept_id, crf"
    )
    if not rs.rows:
        return None
    r = rs.rows[0]
    return {"id": r[0], "kept_id": r[1], "crf": int(r[2] or 23)}


async def _set_compress(db, jid, *, status=None, message=None):
    cols, vals = [], []
    if status is not None:
        cols.append("status=?"); vals.append(status)
    if message is not None:
        cols.append("message=?"); vals.append(message[:300])
    cols.append("updated_at=now_text()")
    vals.append(jid)
    await db.execute(f"UPDATE kept_compress_jobs SET {', '.join(cols)} WHERE id=?", vals)


async def _ffprobe_duration(path) -> float:
    proc = await asyncio.create_subprocess_exec(
        "ffprobe", "-v", "quiet", "-show_entries", "format=duration",
        "-of", "default=nw=1:nk=1", path,
        stdout=asyncio.subprocess.PIPE, stderr=asyncio.subprocess.DEVNULL,
    )
    out, _ = await proc.communicate()
    try:
        return float(out.decode().strip())
    except (ValueError, AttributeError):
        return 0.0


async def _process_compress(db, job):
    """Re-encode a kept file in place (H.264, chosen CRF, veryfast, nice -19). Progress is written
    to kept_compress_jobs.message; the original is only replaced when the result is smaller."""
    jid, kept_id, crf = job["id"], job["kept_id"], job["crf"]
    rs = await db.execute("SELECT rel_path, file_name, size FROM unpack_kept WHERE id=?", [kept_id])
    if not rs.rows:
        await _set_compress(db, jid, status="failed", message="kept file row is gone")
        return
    rel, fname, size = str(rs.rows[0][0]), str(rs.rows[0][1]), int(rs.rows[0][2] or 0)
    src = os.path.join(UNPACK_KEEP, *rel.split("/"))
    if not os.path.exists(src):
        await _set_compress(db, jid, status="failed", message="file no longer on the server")
        return
    try:
        free = shutil.disk_usage(UNPACK_KEEP).free
    except OSError:
        free = None
    if free is not None and free < size:
        await _set_compress(db, jid, status="failed",
                            message=f"not enough disk ({free // 1048576} MB free)")
        return

    tmp = src + ".cmp.mp4"
    duration = await _ffprobe_duration(src)
    cmd = ["ffmpeg", "-y", "-i", src, "-c:v", "libx264", "-crf", str(crf),
           "-preset", "veryfast", "-c:a", "aac", "-b:a", "128k",
           "-movflags", "+faststart", "-f", "mp4", "-progress", "pipe:1",
           "-loglevel", "error", tmp]
    if shutil.which("nice"):
        cmd = ["nice", "-n", "19", *cmd]
    await _set_compress(db, jid, message="compressing… 0%")
    proc = await asyncio.create_subprocess_exec(
        *cmd, stdout=asyncio.subprocess.PIPE, stderr=asyncio.subprocess.PIPE)
    last_pct = -5
    try:
        while True:
            line = await proc.stdout.readline()
            if not line:
                break
            text = line.decode("utf-8", "replace").strip()
            if text.startswith("out_time_us=") and duration > 0:
                try:
                    pct = int(int(text.split("=", 1)[1]) / 1_000_000 / duration * 100)
                except ValueError:
                    continue
                if pct >= last_pct + 5:  # throttle DB writes to every 5%
                    last_pct = pct
                    await _set_compress(db, jid, message=f"compressing… {min(pct, 99)}%")
        await proc.wait()
    finally:
        if proc.returncode is None:
            proc.kill(); await proc.wait()
    if proc.returncode != 0:
        err = (await proc.stderr.read()).decode("utf-8", "replace").strip()[:150]
        _rm(tmp)
        await _set_compress(db, jid, status="failed", message=f"ffmpeg failed: {err}")
        return

    new_size = os.path.getsize(tmp) if os.path.exists(tmp) else 0
    if not new_size or new_size >= size * 0.95:
        _rm(tmp)
        await _set_compress(db, jid, status="done",
                            message="no worthwhile gain — original kept unchanged")
        return
    # Replace the kept file (name becomes .mp4 if it wasn't).
    stem = os.path.splitext(fname)[0]
    new_name = stem + ".mp4"
    new_rel = "/".join(rel.split("/")[:-1] + [new_name])
    dst = os.path.join(UNPACK_KEEP, *new_rel.split("/"))
    os.replace(tmp, dst)
    if dst != src:
        # Carry any uploaded subtitle siblings over to the new name (`<file>.<lang>.vtt`) so a
        # re-encode doesn't orphan them; then drop the old original.
        for sp in _sub_siblings(src):
            try:
                os.replace(sp, dst + sp[len(src):])
            except OSError:
                pass
        _rm(src)
    await db.execute(
        "UPDATE unpack_kept SET file_name=?, rel_path=?, size=? WHERE id=?",
        [new_name, new_rel, new_size, kept_id])
    ready_note = " — ready to upload" if new_size <= PIKPAK_MAX_BYTES else ""
    await _set_compress(db, jid, status="done",
                        message=f"{size / 1073741824:.2f} GB → {new_size / 1073741824:.2f} GB (−{saved}%{ready_note})")
    log.info("Kept-compress job #%s: %s −%s%%", jid, fname, saved)


def _sweep_stale_workdirs():
    """Boot cleanup: after a restart every previously-running job is already marked failed, so any
    leftover <jid>/_work and <jid>/_out temp dirs (possibly GBs of half-downloaded parts) are dead —
    delete them, then drop the <jid> shell if it's empty. Numbered upload-staging subdirs inside
    <jid>/ (sources of still-pending upload_jobs) are untouched, as are _cache/_keep."""
    try:
        names = os.listdir(UNPACK_STAGING)
    except OSError:
        return
    for n in names:
        p = os.path.join(UNPACK_STAGING, n)
        if not n.isdigit() or not os.path.isdir(p):
            continue
        shutil.rmtree(os.path.join(p, "_work"), ignore_errors=True)
        shutil.rmtree(os.path.join(p, "_out"), ignore_errors=True)
        try:
            os.rmdir(p)  # only succeeds if empty
        except OSError:
            pass


async def worker_loop(client, channel, db):
    log.info("Unpack worker started")
    _sweep_stale_workdirs()
    while True:
        try:
            job = await _claim(db)
            if job:
                await _process(client, channel, db, job)
                continue
            cjob = await _claim_compress(db)
            if cjob:
                await _process_compress(db, cjob)
                continue
            _sweep_cache()  # opportunistic: clear abandoned cached archives while idle
            await _sweep_keep(db)  # …and expired kept files (throttled internally)
            await asyncio.sleep(POLL_INTERVAL)
        except asyncio.CancelledError:
            raise
        except Exception as e:  # noqa: BLE001
            log.warning("Unpack worker loop error: %s", e)
            await asyncio.sleep(POLL_INTERVAL)
